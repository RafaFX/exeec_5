
public class Frame {
	private  int id;
	private int tempoCarga;
	private int qtdRef;
	private int tempoRef;
	private int br;
	private int bm;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getTempoCarga() {
		return tempoCarga;
	}
	public void setTempoCarga(int tempoCarga) {
		this.tempoCarga = tempoCarga;
	}
	public int getQtdRef() {
		return qtdRef;
	}
	public void setQtdRef(int qtdRef) {
		this.qtdRef = qtdRef;
	}
	public int getTempoRef() {
		return tempoRef;
	}
	public void setTempoRef(int tempoRef) {
		this.tempoRef = tempoRef;
	}
	public int getBr() {
		return br;
	}
	public void setBr(int br) {
		this.br = br;
	}
	public int getBm() {
		return bm;
	}
	public void setBm(int bm) {
		this.bm = bm;
	}
	
	
	
}
